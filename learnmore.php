      <h2>What is Depression?</h2><hr>
      <p align="justify">Being depressed often feels like carrying a very heavy burden, but you are not alone in this struggle. Many people suffer from some form of depression every year, making it one of the most common mental disorders in the world.<br>
      Gaining a deeper understanding of depression can help begin the journey to recovery. Taking some time to learn more about the causes and symptoms of depression will assist you greatly when it comes time to consider methods of treatment.<br>
      Depression is more than just feeling sad. Everyone feels upset or unmotivated from time to time, but depression is more serious. It is a mood disorder characterized by prolonged feelings of sadness and loss of interest in daily activities. If these symptoms persist for a period of at least two weeks, it is considered a depressive episode.</p>
    <p align="justify"><strong>Types of Depression</strong><br>
    There are many forms of depression. The fifth edition of the Diagnostic and Statistical Manual of Mental Health Disorders lists nine distinct types:<br>
    <p align="justify"><strong>Major depression</strong>, is the most common type of depression. Often, people with major depression experience recurrent episodes throughout their lives.</p>
    <p align="justify"><strong>Dysthymia</strong> is a persistent low mood over a long period of time, even a year or more. It could be described as feeling like you’re living on autopilot.</p>
    <p align="justify"><strong>Seasonal Affective Disorder</strong> is a type of depression brought on from a lack of natural sunlight. Some people are more sensitive to the lower amount of light in the wintertime. </p>
    <p align="justify">Those with <strong>Atypical Depression</strong> often report feeling a heaviness in their limbs. They may suffer from irritability and relationship problems, as well as be prone to overeating and oversleeping.</p>
    <p align="justify"><strong>Bipolar Disorder</strong> is also called <i>Manic Depressive Disorder</i> because it involves alternating between mania and depressive episodes.</p>
    <p align="justify">Sometimes depressive episodes can get so severe that hallucinations or delusions are present, the person becomes catatonic, or they feel stuck in bed. This is known as <b>Psychotic Depression.</b></p>
    <p align="justify"><b>Postpartum Depression</b> occurs after giving birth. Mothers may feel disconnected from their new baby or fear that they will hurt their child.</p>
    <p align="justify">Severe depression that shows up during the second half of the menstrual cycle is called <b>Premenstrual Dysphoric Disorder</b>. It affects the individual’s ability to function normally.</p>
    <p align="justify"><b>Situational Depression</b> is triggered by a life-changing event. It could be anything, from losing your job to the death of an immediate family member.</p>  
    </div>
