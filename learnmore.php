        <h2>Causes</h2><hr>
        <p align="justify">There are a number of factors that may increase the chance of depression, including the following:</p>
        <p align="justify"><b>Abuse:</b> Past physical, sexual, or emotional abuse can increase the vulnerability to clinical depression later in life.</p>
        <p align="justify"><b>Certain medications:</b> Some drugs, such as isotretinoin (used to treat acne), the antiviral drug interferon-alpha, and corticosteroids, can increase your risk of depression.</p>
        <p align="justify"><b>Conflict:</b> Depression in someone who has the biological vulnerability to develop depression may result from personal conflicts or disputes with family members or friends.</p>
        <p align="justify"><b>Death or a loss:</b> Sadness or grief from the death or loss of a loved one, though natural, may increase the risk of depression.</p>
        <p align="justify"><b>Genetics:</b> A family history of depression may increase the risk. It's thought that depression is a complex trait, meaning that there are probably 
          many different genes that each exert small effects, rather than a single gene that contributes to disease risk. The genetics of depression, like most psychiatric 
          disorders, are not as simple or straightforward as in purely genetic diseases such as Huntington's chorea or cystic fibrosis.</p>
        <p align="justify"><b>Major events:</b> Even good events such as starting a new job, graduating, or getting married can lead to depression. So can moving, losing a job or income, getting divorced, or retiring. 
        However, the syndrome of clinical depression is never just a "normal" response to stressful life events.</p>
        <p align="justify"><b>Other personal problems:</b> Problems such as social isolation due to other mental illnesses or being cast out of a family or social group can contribute to the risk of developing clinical depression.</p>
        <p align="justify"><b>Serious illnesses:</b> Sometimes depression co-exists with a major illness or may be triggered by another medical condition.</p>
        <p align="justify"><b>Substance abuse:</b> Nearly 30% of people with substance abuse problems also have major or clinical depression. Even if drugs or alcohol temporarily make you feel better, they ultimately will aggravate depression.</p>
      </div>
      <div class="tab-pane fade" id="list-impact">
        <h2>Impact</h2><hr>
        <p align="justify">Depression can impact every area of your life, including but not limited to how you sleep and eat, your education and career, your relationships, health, and concentration. 
          Individuals suffering from depression often also have comorbid disorders, such as alcohol and drug abuse or other addictions. Depression doesn’t just occur for an individual in vacuum; it can affect your friends, family, co-workers, and everyone around you. 
          In addition, depression may impact how you perform at work or your levels of concentration, so it can negatively affect productivity. Leaving depression untreated can lead to many other complications in one’s personal and professional life. 
          This is why it is so important to seek out help, not just for relationships and work, but for your own sake.</p>
